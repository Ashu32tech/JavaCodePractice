
public class Switch_7 {
		
			public static void testSwith(String a){
				switch(a){
				case "a":
					System.out.println("1");
					break;
				case "b":
					System.out.println("2");
					break;
				case "c":
					System.out.println("3");
					break;
				default:
					System.out.println("default");
				}
				
				
			}
			public static void main(String args[]){
				Switch_7.testSwith("a");
			}
	
}
